package p0203;

public class DataType {
	
	public static void main(String[] args) {
		//	기본형 참조형
		//	기본형 : 숫자, 문자, 논리
		//	숫자 : 정수, 실수
		//	정수
		byte b = 1; // (byte)1이지만 casting 되므로 생략함
		short s;
		int i;
		long l = 1;
		//	실수
		float f = (float)1.1;	// float f = 1.1F; 도 가능
		double db;
		//	문자
		char c;
		//	논리
		boolean bl;
		
	}
}
