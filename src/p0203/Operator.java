package p0203;

public class Operator {

	public static void main(String[] args) {
		//산술연산자
		int n1 = 10;
		int n2 = 20;
		
		int result = n1 + n2;
		System.out.println(result);
		
		result = n1 - n2;
		System.out.println(result);
		
		result = n1 * n2;
		System.out.println(result);
		
		result = n1 / n2;
		System.out.println(result);
		
		result  = n1 % n2;
		System.out.println(result);
	}
}
