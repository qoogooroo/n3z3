package p0203;

public class Operator2 {

	public static void main(String[] args) {
		// 비교연산자
		int n1 = 1;
		int n2 = 2;
		
		boolean result = n1 == n2;
		System.out.println(result);
		
		result = n1 != n2;
		System.out.println(result);
		
		result =  n1 > n2;
		System.out.println(result);
		
		result = n1 < n2;
		System.out.println(result);
		
		result = n1 >= n2;
		System.out.println(result);
		
		result = n1 <= n2;
		System.out.println(result);
		
	}
}
